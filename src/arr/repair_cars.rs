/*
2594. 修车的最少时间
提示
中等
54
相关企业
给你一个整数数组 ranks ，表示一些机械工的 能力值 。ranksi 是第 i 位机械工的能力值。能力值为 r 的机械工可以在 r * n^2 分钟内修好 n 辆车。

同时给你一个整数 cars ，表示总共需要修理的汽车数目。

请你返回修理所有汽车 最少 需要多少时间。

注意：所有机械工可以同时修理汽车。



示例 1：

输入：ranks = [4,2,3,1], cars = 10
输出：16
解释：
- 第一位机械工修 2 辆车，需要 4 * 2 * 2 = 16 分钟。
- 第二位机械工修 2 辆车，需要 2 * 2 * 2 = 8 分钟。
- 第三位机械工修 2 辆车，需要 3 * 2 * 2 = 12 分钟。
- 第四位机械工修 4 辆车，需要 1 * 4 * 4 = 16 分钟。
16 分钟是修理完所有车需要的最少时间。
示例 2：

输入：ranks = [5,1,8], cars = 6
输出：16
解释：
- 第一位机械工修 1 辆车，需要 5 * 1 * 1 = 5 分钟。
- 第二位机械工修 4 辆车，需要 1 * 4 * 4 = 16 分钟。
- 第三位机械工修 1 辆车，需要 8 * 1 * 1 = 8 分钟。
16 分钟时修理完所有车需要的最少时间。


提示：

1 <= ranks.length <= 105
1 <= ranks[i] <= 100
1 <= cars <= 106
 */
struct Solution;

/*
解的值域范围内有单调性，就可以使用二分：

假设 t 分钟内可以将所有汽车都修理完，那么大于等于 t 分钟内都可以将所有汽车修理完。
假设 t 分钟内不能够将所有汽车都修理完，那么小于等于 t 分钟内也不能够将所有汽车修理完。

给定时间t,  能力为r的机械工可以修理 [sqrt(t/r)]
 */
impl Solution {
    pub fn repair_cars(ranks: Vec<i32>, cars: i32) -> i64 {
        let (mut l, mut r) = (1_i64, ranks[0] as i64 * cars as i64 * cars as i64);
        let check = |t: i64| {
            ranks.iter().map(|&r| (t as f64 / r as f64).sqrt() as i64)
                .sum::<i64>() >= cars as i64
        };
        while l < r {
            let mid = (l + r) >> 1;
            if check(mid) {
                // 缩小时间
                r = mid;
            } else {
                // 加大时间
                l = mid + 1;
            }
        }
        l
    }
}

